package com.cg.controller;

//@XmlRootElement
public class Track {
	
	String title;
	String singer;

	public Track() {
		super();
	}

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public String getSinger() {
		return singer;
	}

	public void setSinger(String singer) {
		this.singer = singer;
	}
	
	

}
